/**
 * add - sums a and b
 */
int add(int a, int b)
{
	return a + b;
}

/**
 * sub - subtracts b from a
 */
int sub(int a, int b)
{
	return a - b;
}

/**
 * mul - multiplies a and b
 */
int mul(int a, int b)
{
	return a * b;
}

/**
 * div - divides a and b
 */
float div(float a, float b)
{
	return a / b;
}

/**
 * mod - returns a modulo absolute value of b
 */
int mod(int a, int b)
{
	return a % b;
}
